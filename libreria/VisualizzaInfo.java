    /**
     * Gestisce i click sul conferma modifiche.
     * se Ã¨ selezionato l'eliminazione esegue l'eliminazione dell'elemento
     * in caso contrario aggiorna i dati.
     * 
     * @param evt 
     */
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if(checkbox1.getState()){
        
            lib.elimina(pos);
        } else{
        elem.setAnno(JTextAnno.getText());
        elem.setAutore(JTextAutore.getText());
        elem.setTitolo(JTextTitolo.getText());
        }
        modello.aggiorna();
        this.dispose();
        
        
    }//GEN-LAST:event_jButton4ActionPerformed
